<?php
// Declare a variable called "price" to store the original price of the item which is 50
$price = 50; 

// Declare a variable called "discount " to store the discount amount which is 10
$discount = 10; 

// Calculate the final price after applying the discount. This is done by subtracting the discount from the original price.
$finalPrice = $price - $discount; 

// Display the total price after discount
echo "Total price: $" . $finalPrice;
?>
